package com.shenjianli.fly.map;

public class ShowMapData {

	private String hotelAddrCity;
	private String hotelAddrCounty;
	private String hotelName;
	private String hotelAddrStreet;
	
	private float mapLevel; 
	
	public String getHotelAddrStreet() {
		return hotelAddrStreet;
	}
	public void setHotelAddrStreet(String hotelAddrStreet) {
		this.hotelAddrStreet = hotelAddrStreet;
	}
	public String getHotelAddrCity() {
		return hotelAddrCity;
	}
	public void setHotelAddrCity(String hotelAddrCity) {
		this.hotelAddrCity = hotelAddrCity;
	}
	public String getHotelAddrCounty() {
		return hotelAddrCounty;
	}
	public void setHotelAddrCounty(String hotelAddrCounty) {
		this.hotelAddrCounty = hotelAddrCounty;
	}
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public float getMapLevel() {
		return mapLevel;
	}
	public void setMapLevel(float mapLevel) {
		this.mapLevel = mapLevel;
	}
	
}
